
Format pliku indeksowego do liczenia RDF. Zakładam, że RDF może być liczony
między wskazanymi grupami atomów (główne zastosowanie) ale czasami z 
wyłączeniem wkładu pewnych par: np. liczymy RDF dla oddziaływań 
międzycząsteczkowych wyłącznie.

 atoms:: w tej grupie podane są indeksy atomów (1..n) z wszystkich 
 analizowanych grup; format dowolny: lista liczb naturalnych

 moieties:: w tej grupie podane są liczby atomów z listy __atoms__, które
 razem tworzą rozróżnialną całość (np. reszta, cząsteczka, grupa ale także
 zbiór np. jonów); format dowolny: lista liczb naturalnych

 pairs:: indeksy par do listy __moieties__ dla których będzie liczona RDF


 
